#include "libft.h"

char		*ft_strncpy(char *s1, const char *s2, size_t len)
{
	size_t	i;

	i = 0;
	while (s2[i] && i < len)
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
